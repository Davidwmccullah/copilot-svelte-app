	<h3>Principle of Least Astonishment</h3>
	<p>
		When two elements of an interface conflict, the result should be the least surprising of the
		possible outcomes.
	</p>

	<h3>Principle of Least Privilege</h3>
	<p>
		Users should only be given the minimum level of access necessary to perform their tasks. This
		reduces the risk of unauthorized access and potential damage.
	</p>

	<h3>Principle of Least Common Mechanism</h3>
	<p>
		Shared mechanisms should be minimized to reduce the potential impact of failures or security
		breaches. This principle promotes modularity and encapsulation.
	</p>

	<h3>Principle of Least Effort</h3>
	<p>
		Design should minimize the effort required by users to accomplish their tasks. This includes
		reducing the number of steps, simplifying complex processes, and providing clear instructions.
	</p>

	<h3>Principle of Least Knowledge</h3>
	<p>
		Modules, classes, and components should have limited knowledge about other parts of the system.
		This reduces dependencies and improves maintainability.
	</p>

	<h3>Principle of Least Surprise</h3>
	<p>
		Software behavior should align with users' expectations. Actions and outcomes should be
		consistent and intuitive, minimizing confusion and errors.
	</p>

